@dataclass
class FileRecord:
    relative_path: str
    language: str | None
    file_kind: str   # source, config, doc
    size_bytes: int
    line_count: int
    included: bool
    skip_reason: str | None


@dataclass
class Chunk:
    file_path: str       # relative path within repo
    language: str | None
    chunk_type: str      # class, function, method, section, file
    symbol_name: str | None
    start_line: int
    end_line: int
    content: str
    content_hash: str = field(default="")

    def __post_init__(self) -> None:
        if not self.content_hash:
            self.content_hash = hashlib.sha256(self.content.encode()).hexdigest()



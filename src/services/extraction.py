def _is_excluded_dir(path_part: str) -> bool:
    return path_part in EXCLUDED_DIRS or path_part.endswith(".egg-info")


def inspect_repo_files(repo_path: Path) -> Iterator[FileRecord]:
    """Enumerate and classify all files in a cloned repo."""
    for file_path in repo_path.rglob("*"):
        if not file_path.is_file():
            continue

        # Check excluded directory components
        try:
            rel = file_path.relative_to(repo_path)
        except ValueError:
            continue

        parts = rel.parts
        if any(_is_excluded_dir(p) for p in parts[:-1]):
            continue

        filename = file_path.name
        suffix = file_path.suffix.lower()

        # Excluded filenames
        if filename in EXCLUDED_FILENAMES:
            yield FileRecord(
                relative_path=str(rel),
                language=None,
                file_kind="noise",
                size_bytes=0,
                line_count=0,
                included=False,
                skip_reason="excluded_filename",
            )
            continue

        # Excluded extensions
        if suffix in EXCLUDED_EXTENSIONS:
            yield FileRecord(
                relative_path=str(rel),
                language=None,
                file_kind="binary_or_noise",
                size_bytes=0,
                line_count=0,
                included=False,
                skip_reason="excluded_extension",
            )
            continue

        # Size check
        try:
            size = file_path.stat().st_size
        except OSError:
            yield FileRecord(
                relative_path=str(rel),
                language=None,
                file_kind="unknown",
                size_bytes=0,
                line_count=0,
                included=False,
                skip_reason="stat_error",
            )
            continue

        if size > MAX_FILE_BYTES:
            yield FileRecord(
                relative_path=str(rel),
                language=None,
                file_kind="oversized",
                size_bytes=size,
                line_count=0,
                included=False,
                skip_reason="file_too_large",
            )
            continue

        # Detect language
        language = INCLUDED_EXTENSIONS.get(suffix) or VALUABLE_FILENAMES.get(filename)
        if language is None:
            # Try by lowercase suffix match
            language = INCLUDED_EXTENSIONS.get(file_path.suffix)

        if language is None:
            yield FileRecord(
                relative_path=str(rel),
                language=None,
                file_kind="unknown",
                size_bytes=size,
                line_count=0,
                included=False,
                skip_reason="unsupported_type",
            )
            continue

        # Read and count lines, check for binary content
        try:
            text = file_path.read_text(encoding="utf-8", errors="replace")
            # Heuristic: if many null bytes, treat as binary
            if text.count("\x00") > len(text) * 0.01:
                yield FileRecord(
                    relative_path=str(rel),
                    language=None,
                    file_kind="binary",
                    size_bytes=size,
                    line_count=0,
                    included=False,
                    skip_reason="binary_content",
                )
                continue
            line_count = text.count("\n") + (1 if text and not text.endswith("\n") else 0)
        except Exception as exc:
            yield FileRecord(
                relative_path=str(rel),
                language=language,
                file_kind="source",
                size_bytes=size,
                line_count=0,
                included=False,
                skip_reason=f"read_error:{exc}",
            )
            continue

        # Generated file detection: scan header region only
        header = text[:_GENERATED_HEADER_BYTES]
        if any(marker in header for marker in _GENERATED_MARKERS):
            yield FileRecord(
                relative_path=str(rel),
                language=language,
                file_kind="generated",
                size_bytes=size,
                line_count=line_count,
                included=False,
                skip_reason="generated_file",
            )
            continue

        file_kind = _classify_file_kind(str(rel), language)

        yield FileRecord(
            relative_path=str(rel),
            language=language,
            file_kind=file_kind,
            size_bytes=size,
            line_count=line_count,
            included=True,
            skip_reason=None,
        )


def _classify_file_kind(relative_path: str, language: str) -> str:
    path_lower = relative_path.lower()
    if language in ("markdown", "rst", "text"):
        return "doc"
    if language in ("yaml", "toml", "json", "ini", "xml", "dockerfile", "makefile", "terraform", "hcl"):
        return "config"
    if language in ("shell",):
        return "script"
    if any(p in path_lower for p in ("test/", "tests/", "spec/", "specs/", "__tests__")):
        return "test"
    return "source"



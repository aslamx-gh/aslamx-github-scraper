"""Validation, deduplication, and quarantine for extracted chunks."""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass, field
from pathlib import Path

from ..database import get_connection

logger = logging.getLogger(__name__)

# Quarantine reason constants
REASON_EXACT_DUPLICATE = "exact_duplicate"
REASON_BOILERPLATE = "boilerplate"
REASON_PLACEHOLDER = "placeholder"
REASON_LICENSE_HEADER = "license_header"
REASON_GENERATED_MARKER = "generated_marker"

# Cheap content-based heuristics: license header prefixes (lowercased, stripped)
_LICENSE_PREFIXES = (
    "# mit license",
    "# copyright",
    "# spdx-license-identifier",
    "/* copyright",
    "// copyright",
    "-- copyright",
    "# license:",
    "# this file is part of",
)

# Cheap content-based heuristics: generated-file markers
_GENERATED_MARKERS = (
    "# this file is auto-generated",
    "# do not edit",
    "# do not modify",
    "// generated by",
    "// code generated",
    "/* do not edit",
    "# generated by",
    "autogenerated file",
)


@dataclass
class ValidationReport:
    """Summary returned by validate_pending_chunks()."""
    snapshot_id: int | None
    pending_count: int
    accepted: int = 0
    quarantined: int = 0
    by_reason: dict[str, int] = field(default_factory=dict)
    errors: list[str] = field(default_factory=list)

    def to_dict(self) -> dict:
        return {
            "snapshot_id": self.snapshot_id,
            "pending_count": self.pending_count,
            "accepted": self.accepted,
            "quarantined": self.quarantined,
            "by_reason": self.by_reason,
            "errors": self.errors,
        }


def validate_pending_chunks(
    db_path: Path | None = None,
    snapshot_id: int | None = None,
) -> ValidationReport:
    """
    Validate all pending chunks, optionally scoped to a single snapshot.

    Only processes chunks with validation_status='pending' — idempotent by design.
    Exact-duplicate detection compares content_hash against all non-quarantined chunks
    in the DB, so duplicates across different repos are caught.

    Quarantine checks (first match wins):
      1. exact_duplicate  — same content_hash exists elsewhere in the DB
      2. boilerplate      — very_short + mostly_comments quality flags both set
      3. placeholder      — has_placeholder quality flag set
      4. license_header   — content starts with a known license prefix (if readable)
      5. generated_marker — content contains a generated-file marker (if readable)

    Returns a ValidationReport with counts and per-reason breakdown.
    """
    conn = get_connection(db_path)
    report = ValidationReport(snapshot_id=snapshot_id, pending_count=0)

    try:
        if snapshot_id is not None:
            rows = conn.execute(
                """SELECT chunk_id, content_hash, quality_flags,
                          longevity_band, primary_tag
                   FROM chunks
                   WHERE validation_status = 'pending' AND snapshot_id = ?""",
                (snapshot_id,),
            ).fetchall()
        else:
            rows = conn.execute(
                """SELECT chunk_id, content_hash, quality_flags,
                          longevity_band, primary_tag
                   FROM chunks
                   WHERE validation_status = 'pending'""",
            ).fetchall()

        report.pending_count = len(rows)
        if not rows:
            return report

        for row in rows:
            chunk_id = row["chunk_id"]
            content_hash = row["content_hash"]
            try:
                flags = json.loads(row["quality_flags"] or "[]")
            except (ValueError, TypeError):
                flags = []

            quarantine_reason = _classify_chunk(
                conn, chunk_id, content_hash, flags,
                row["longevity_band"], row["primary_tag"],
            )

            if quarantine_reason:
                try:
                    conn.execute(
                        "UPDATE chunks SET validation_status='quarantined', quarantine_reason=? WHERE chunk_id=?",
                        (quarantine_reason, chunk_id),
                    )
                    conn.commit()
                    report.quarantined += 1
                    report.by_reason[quarantine_reason] = (
                        report.by_reason.get(quarantine_reason, 0) + 1
                    )
                    from .storage import write_quarantine_artifact
                    write_quarantine_artifact(chunk_id, content_hash, quarantine_reason)
                except Exception as exc:
                    logger.warning("Failed to quarantine chunk %d: %s", chunk_id, exc)
                    report.errors.append(f"chunk {chunk_id}: {exc}")
            else:
                try:
                    conn.execute(
                        "UPDATE chunks SET validation_status='accepted' WHERE chunk_id=?",
                        (chunk_id,),
                    )
                    conn.commit()
                    report.accepted += 1
                except Exception as exc:
                    logger.warning("Failed to accept chunk %d: %s", chunk_id, exc)
                    report.errors.append(f"chunk {chunk_id}: {exc}")

    except Exception as exc:
        logger.error("Validation pipeline error: %s", exc)
        report.errors.append(str(exc))
    finally:
        conn.close()

    logger.info(
        "Validation complete (snapshot=%s): %d pending → %d accepted, %d quarantined, %d errors",
        snapshot_id, report.pending_count, report.accepted, report.quarantined, len(report.errors),
    )
    return report


def get_validation_summary(db_path: Path | None = None) -> dict:
    """Return aggregate validation stats across all chunks."""
    conn = get_connection(db_path)
    try:
        total = conn.execute("SELECT COUNT(*) as cnt FROM chunks").fetchone()["cnt"]
        status_rows = conn.execute(
            "SELECT validation_status, COUNT(*) as cnt FROM chunks GROUP BY validation_status"
        ).fetchall()
        reason_rows = conn.execute(
            """SELECT quarantine_reason, COUNT(*) as cnt FROM chunks
               WHERE validation_status = 'quarantined' AND quarantine_reason IS NOT NULL
               GROUP BY quarantine_reason"""
        ).fetchall()
        pending = conn.execute(
            "SELECT COUNT(*) as cnt FROM chunks WHERE validation_status = 'pending'"
        ).fetchone()["cnt"]
        return {
            "total_chunks": total,
            "pending_chunks": pending,
            "by_status": {r["validation_status"]: r["cnt"] for r in status_rows},
            "quarantine_by_reason": {r["quarantine_reason"]: r["cnt"] for r in reason_rows},
        }
    finally:
        conn.close()



# Internal helpers
# ---------------------------------------------------------------------------

def _classify_chunk(
    conn,
    chunk_id: int,
    content_hash: str,
    flags: list[str],
    longevity_band: str | None,
    primary_tag: str | None,
) -> str | None:
    """Return a quarantine reason string, or None to accept the chunk."""

    # 1. Exact duplicate — cross-repo
    if _is_exact_duplicate(conn, content_hash, chunk_id):
        return REASON_EXACT_DUPLICATE

    # 2. Boilerplate: very_short + mostly_comments
    if "very_short" in flags and "mostly_comments" in flags:
        return REASON_BOILERPLATE

    # 3. Placeholder content
    if "has_placeholder" in flags:
        return REASON_PLACEHOLDER

    # 4 & 5. Cheap content-based heuristics (skipped gracefully if unreadable)
    content = _read_chunk_content(chunk_id, longevity_band, primary_tag)
    if content:
        content_lower = content.lower().strip()
        if any(content_lower.startswith(p) for p in _LICENSE_PREFIXES):
            return REASON_LICENSE_HEADER
        if any(m in content_lower for m in _GENERATED_MARKERS):
            return REASON_GENERATED_MARKER

    return None


def _is_exact_duplicate(conn, content_hash: str, chunk_id: int) -> bool:
    """Return True if a non-quarantined chunk with the same hash already exists."""
    row = conn.execute(
        """SELECT 1 FROM chunks
           WHERE content_hash = ?
             AND chunk_id != ?
             AND validation_status != 'quarantined'
           LIMIT 1""",
        (content_hash, chunk_id),
    ).fetchone()
    return row is not None


def _read_chunk_content(
    chunk_id: int,
    longevity_band: str | None,
    primary_tag: str | None,
) -> str | None:
    """
    Try to read chunk content from its curated artifact file.
    Returns None if the file cannot be found or read (non-fatal).
    """
    try:
        from .manifests import EXTRACTED_DIR, _safe_name, VALID_BANDS
        band = longevity_band if longevity_band in VALID_BANDS else "small"
        tag = _safe_name(primary_tag or "general")
        tag_dir = EXTRACTED_DIR / band / tag
        if not tag_dir.exists():
            return None
        # Artifact filenames include the chunk_id: {safe_repo}_{snap}_{chunk_id}_{sym}.txt
        matches = list(tag_dir.glob(f"*_{chunk_id}_*.txt"))
        if not matches:
            return None
        text = matches[0].read_text(encoding="utf-8", errors="replace")
        # Strip provenance header: --- \n {JSON} \n --- \n\n {content}
        parts = text.split("---", 2)
        if len(parts) >= 3:
            return parts[2].strip()
        return text
    except Exception:
        return None
